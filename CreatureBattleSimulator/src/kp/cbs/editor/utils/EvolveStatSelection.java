    private void init()
    {
        setResizable(false);
        Utils.focus(this);
        
        this.stat0.setModel(new DefaultComboBoxModel<>(StatId.values()));
        this.stat1.setModel(new DefaultComboBoxModel<>(StatId.values()));
        
        this.cmpText.setText(cond.getType() == EvolutionConditionType.STAT_GREATER_THAN ? "Mayor que" : "Igual a");
        this.stat0.setSelectedItem(cond.getFirstStat());
        this.stat1.setSelectedItem(cond.getSecondStat());
    }
    
    public static final void open(RaceEditor parent, EvolveCondition cond)
    {
        if(cond instanceof StatEvolveCondition)
        {
            var sel = new EvolveStatSelection(parent, (StatEvolveCondition) cond);
            sel.setVisible(true);
        }
    }

